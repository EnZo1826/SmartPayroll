function updateDashboard() {
  document.getElementById('dash-emp-count').textContent = employees.length;
  document.getElementById('emp-count-badge').textContent = employees.length;
  initReportSelectors();
  // Latest run stats
  const now = new Date();
  const m=now.getMonth()+1, y=now.getFullYear();
  const run = payrollRuns.find(r=>r.month===m&&r.year===y)||payrollRuns[payrollRuns.length-1];
  if(run&&run.results) {
    const gross=(run.results).reduce((s,r)=>s+(r.calc?.grossEarnings||0),0);
    const net=(run.results).reduce((s,r)=>s+(r.calc?.netPay||0),0);
    const tax=(run.results).reduce((s,r)=>s+(r.calc?.paye||0)+(r.calc?.sscEmployee||0),0);
    document.getElementById('dash-gross-total').textContent=fmt(gross);
    document.getElementById('dash-net-total').textContent=fmt(net);
    document.getElementById('dash-tax-total').textContent=fmt(tax);
  } else {
    document.getElementById('dash-gross-total').textContent='N$0.00';
    document.getElementById('dash-net-total').textContent='N$0.00';
    document.getElementById('dash-tax-total').textContent='N$0.00';
  }
  // Recent runs
  const recentDiv = document.getElementById('dash-recent-runs');
  if(!payrollRuns.length) { recentDiv.innerHTML='<p class="italic">No payroll runs yet.</p>'; return; }
  recentDiv.innerHTML = payrollRuns.slice(-5).reverse().map(r=>{
    const net=(r.results||[]).reduce((s,x)=>s+(x.calc?.netPay||0),0);
    return `<div class="flex items-center justify-between py-2 border-b border-slate-100 dark:border-slate-700 last:border-0">
      <span class="font-medium text-slate-900 dark:text-white text-sm">${monthName(r.month)} ${r.year}</span>
      <span class="text-sm font-mono text-green-600 dark:text-green-400">${fmt(net)}</span>
    </div>`;
  }).join('');
}


