// Calculate annual PAYE on annual taxable income
function calcAnnualPAYE(annualIncome) {
  if (annualIncome <= 100000) return 0;
  if (annualIncome <= 150000) return (annualIncome - 100000) * 0.18;
  if (annualIncome <= 350000) return 9000 + (annualIncome - 150000) * 0.25;
  if (annualIncome <= 550000) return 59000 + (annualIncome - 350000) * 0.28;
  if (annualIncome <= 850000) return 115000 + (annualIncome - 550000) * 0.30;
  if (annualIncome <= 1550000) return 205000 + (annualIncome - 850000) * 0.32;
  return 429000 + (annualIncome - 1550000) * 0.37;
}

// Monthly PAYE: annualise gross earnings, compute annual tax, divide by 12
function calcMonthlyPAYE(monthlyGross) {
  const annual = monthlyGross * 12;
  const annualTax = calcAnnualPAYE(annual);
  return Math.round((annualTax / 12) * 100) / 100;
}

// SSC Employee: 0.9% of basic salary
// Ceiling at N$11,000 → max N$99.00; floor at N$500 → min N$4.50
function calcSSC(basicSalary) {
  // Apply ceiling/floor to the contribution base
  let base = Math.min(basicSalary, 11000);
  base = Math.max(base, 500);
  let ssc = base * 0.009;
  ssc = Math.min(ssc, 99.00);
  ssc = Math.max(ssc, 4.50);
  return Math.round(ssc * 100) / 100;
}

// Minimum wage check: N$18/hr × 195 hrs = N$3,510/month
const MIN_WAGE_MONTHLY = 18 * 195; // 3510

// Hourly rate from monthly salary (195 hrs/month)
function hourlyRate(basicSalary) {
  return basicSalary / 195;
}

// OT pay: normal 1.5×, Sunday/PH 2×
function calcOTPay(basicSalary, otNormalHrs, otSunHrs) {
  const hr = hourlyRate(basicSalary);
  return (otNormalHrs * hr * 1.5) + (otSunHrs * hr * 2);
}

// Full employee payroll calculation
function calcEmployeePayroll(emp, entry) {
  let basic;
  if (emp.payType === 'hourly') {
    const hrs = parseFloat(entry.hoursWorked) || 0;
    const rate = parseFloat(emp.hourlyRate) || 0;
    basic = hrs * rate;
  } else {
    basic = parseFloat(emp.basicSalary) || 0;
  }
  const otNormal = parseFloat(entry.otNormal) || 0;
  const otSun = parseFloat(entry.otSun) || 0;
  // For hourly employees OT is based on their hourly rate, for salaried on derived rate
  const baseHourlyRate = emp.payType === 'hourly' ? (parseFloat(emp.hourlyRate)||0) : hourlyRate(basic);
  const otPay = (otNormal * baseHourlyRate * 1.5) + (otSun * baseHourlyRate * 2);
  const allowances = (entry.allowances || []).reduce((s,a)=>s+(parseFloat(a.amount)||0),0);
  const otherEarnings = parseFloat(entry.otherEarnings) || 0;
  const grossEarnings = basic + otPay + allowances + otherEarnings;
  const sscEmployee = calcSSC(basic);
  const paye = calcMonthlyPAYE(grossEarnings);
  const otherDeductions = (entry.deductions || []).reduce((s,d)=>s+(parseFloat(d.amount)||0),0);
  const totalDeductions = sscEmployee + paye + otherDeductions;
  const netPay = grossEarnings - totalDeductions;
  const employerSSC = sscEmployee;
  const employerTotalCost = basic + employerSSC;
  return { basic, otNormal, otSun, otPay, allowances, otherEarnings, grossEarnings, sscEmployee, paye, otherDeductions, totalDeductions, netPay, employerSSC, employerTotalCost };
}

// YTD months elapsed since tax year start for a given payroll period
function ytdMonthsElapsed(runMonth, runYear) {
  const startMonth = parseInt(settings.taxYearStart)||3; // default March
  // Determine the tax year start date for this run's period
  // If runMonth >= startMonth → same calendar year start; else previous year start
  let taxYearStartMonth = startMonth;
  let taxYearStartYear = (runMonth >= startMonth) ? runYear : runYear - 1;
  // Count months from start to current (inclusive)
  let months = (runYear - taxYearStartYear) * 12 + (runMonth - taxYearStartMonth) + 1;
  return Math.max(1, months);
}

// Tax year start/end label for display (e.g. "Mar 2025 – Feb 2026")
function taxYearLabel(runMonth, runYear) {
  const startMonth = parseInt(settings.taxYearStart)||3;
  const endMonth = startMonth === 1 ? 12 : startMonth - 1;
  const startYear = (runMonth >= startMonth) ? runYear : runYear - 1;
  const endYear = startMonth === 1 ? startYear : startYear + 1;
  return `${monthName(startMonth)} ${startYear} – ${monthName(endMonth)} ${endYear}`;
}

