function updateEntry(empId, field, value) {
  if(!payrollEntries[empId]) payrollEntries[empId]={otNormal:0,otSun:0,hoursWorked:0,allowances:[],otherEarnings:0,deductions:[]};
  payrollEntries[empId][field] = parseFloat(value)||0;
  const emp = employees.find(e=>e.id===empId);
  if(!emp) return;
  const calc = calcEmployeePayroll(emp, payrollEntries[empId]);
  const basicCell = document.getElementById(`pr-basic-${empId}`);
  if(basicCell) basicCell.textContent = fmt(calc.basic);
  document.getElementById(`pr-otpay-${empId}`).textContent = fmt(calc.otPay);
  document.getElementById(`pr-allow-${empId}`).textContent = fmt(calc.allowances);
  document.getElementById(`pr-gross-${empId}`).textContent = fmt(calc.grossEarnings);
  document.getElementById(`pr-ssc-${empId}`).textContent = fmt(calc.sscEmployee);
  document.getElementById(`pr-paye-${empId}`).textContent = fmt(calc.paye);
  document.getElementById(`pr-oded-${empId}`).textContent = fmt(calc.otherDeductions);
  document.getElementById(`pr-net-${empId}`).textContent = fmt(calc.netPay);
  updatePayrollTotals();
}

function updatePayrollTotals() {
  let totBasic=0,totAllow=0,totOtherEarn=0,totGross=0,totSSC=0,totPAYE=0,totODed=0,totNet=0;
  employees.forEach(emp=>{
    const entry = payrollEntries[emp.id]||{};
    const c = calcEmployeePayroll(emp,entry);
    totBasic+=c.basic; totAllow+=c.allowances; totOtherEarn+=c.otherEarnings;
    totGross+=c.grossEarnings; totSSC+=c.sscEmployee; totPAYE+=c.paye;
    totODed+=c.otherDeductions; totNet+=c.netPay;
  });
  document.getElementById('tot-basic').textContent = fmt(totBasic);
  document.getElementById('tot-allow').textContent = fmt(totAllow);
  document.getElementById('tot-other-earn').textContent = fmt(totOtherEarn);
  document.getElementById('tot-gross').textContent = fmt(totGross);
  document.getElementById('tot-ssc').textContent = fmt(totSSC);
  document.getElementById('tot-paye').textContent = fmt(totPAYE);
  document.getElementById('tot-other-ded').textContent = fmt(totODed);
  document.getElementById('tot-net').textContent = fmt(totNet);
}

