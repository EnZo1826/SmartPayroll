function importCSV(event) {
  const file = event.target.files[0]; if(!file) return;
  const reader = new FileReader();
  reader.onload = function(e){
    const lines = e.target.result.split('\n').filter(l=>l.trim());
    const headerRow = lines[0].toLowerCase();
    const hasPayType = headerRow.includes('pay type') || headerRow.includes('paytype');
    const rows = lines.slice(1).map(l=>parseCSVRow(l));
    let count=0;
    rows.forEach(cols=>{
      if(cols.length<4||!cols[0]) return;
      let payType='monthly', basicSalary=0, hourlyRate=0, bankName='', accountNo='', taxNo='', startDate='';
      if(hasPayType && cols.length>=6) {
        payType=(cols[3]||'monthly').toLowerCase().trim();
        basicSalary=payType==='monthly'?(parseFloat(cols[4])||0):0;
        hourlyRate=payType==='hourly'?(parseFloat(cols[5])||0):0;
        bankName=cols[6]||''; accountNo=cols[7]||''; taxNo=cols[8]||''; startDate=cols[9]||'';
      } else {
        basicSalary=parseFloat(cols[3])||0;
        bankName=cols[4]||''; accountNo=cols[5]||''; taxNo=cols[6]||''; startDate=cols[7]||'';
      }
      const leaveBalanceVal = hasPayType && cols.length>=12 ? (parseFloat(cols[10])||0) : (settings.defaultLeave||20);
      const leaveTakenVal   = hasPayType && cols.length>=12 ? (parseFloat(cols[11])||0) : 0;
      employees.push({
        id:'emp_'+Date.now()+'_'+Math.random().toString(36).slice(2,6),
        name:cols[0]||'', empId:cols[1]||'', position:cols[2]||'',
        payType, basicSalary, hourlyRate, bankName, accountNo, taxNo, startDate,
        leaveBalance: leaveBalanceVal, leaveTaken: leaveTakenVal
      });
      count++;
    });
    saveEmployees(); renderEmployees(); updateDashboard();
    toast(`Imported ${count} employees.`,'green');
    event.target.value='';
  };
  reader.readAsText(file);
}

function parseCSVRow(row) {
  const cols=[]; let inQ=false,cur='';
  for(let i=0;i<row.length;i++){
    const ch=row[i];
    if(ch==='"'){ if(inQ&&row[i+1]==='"'){cur+='"';i++;}else{inQ=!inQ;} }
    else if(ch===','&&!inQ){ cols.push(cur.trim()); cur=''; }
    else { cur+=ch; }
  }
  cols.push(cur.trim()); return cols;
}

