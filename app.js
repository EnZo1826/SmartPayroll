// ── Execute Import ──────────────────────────────────────────
function executeImport() {
  const d = window._importData;
  if (!d) return;

  try {
    if (d.type === 'csv') {
      importFromCSV(d.content);
    } else if (d.type === 'json') {
      importFromJSON(d.parsed);
    }
    closeIOModal();
    resetImportPanel();
  } catch (err) {
    toast('Import failed: ' + err.message, 'red');
  }
}

function importFromCSV(content) {
  const lines = content.trim().split('\n').filter(l => l.trim());
  if (lines.length < 2) { toast('CSV has no data rows.','red'); return; }
  // Detect if CSV has new format (10 cols) or old format (8 cols) by header
  const headerRow = lines[0].toLowerCase();
  const hasPayType = headerRow.includes('pay type') || headerRow.includes('paytype');
  let count = 0, updated = 0;
  lines.slice(1).forEach(line => {
    const cols = parseCSVRow(line);
    let name, empId, position, payType, basicSalary, hourlyRate, bankName, accountNo, taxNo, startDate, leaveBalance, leaveTaken;
    if (hasPayType) {
      [name, empId, position, payType, basicSalary, hourlyRate, bankName, accountNo, taxNo, startDate, leaveBalance, leaveTaken] = cols;
    } else {
      [name, empId, position, basicSalary, bankName, accountNo, taxNo, startDate] = cols;
      payType = 'monthly'; hourlyRate = 0; leaveBalance = settings.defaultLeave||20; leaveTaken = 0;
    }
    if (!name || !empId) return;
    const pt = (payType||'monthly').trim().toLowerCase();
    const existing = employees.findIndex(e => e.empId === empId);
    const emp = {
      id: existing >= 0 ? employees[existing].id : Date.now() + Math.random(),
      name: name.trim(), empId: empId.trim(), position: (position||'').trim(),
      payType: pt === 'hourly' ? 'hourly' : 'monthly',
      basicSalary: pt === 'monthly' ? (parseFloat(basicSalary)||0) : 0,
      hourlyRate:  pt === 'hourly'  ? (parseFloat(hourlyRate)||0) : 0,
      bankName: (bankName||'').trim(), accountNo: (accountNo||'').trim(),
      taxNo: (taxNo||'').trim(), startDate: (startDate||'').trim(),
      leaveBalance: parseFloat(leaveBalance)||0,
      leaveTaken: parseFloat(leaveTaken)||0
    };
    if (existing >= 0) { employees[existing] = emp; updated++; } else { employees.push(emp); count++; }
  });
  saveEmployees();
  updateDashboard(); renderEmployees(); renderPayslips();
  toast(`Imported ${count} new · Updated ${updated} employees.`, 'green');
}

function importFromJSON(parsed) {
  const type = parsed._type || (parsed.employees && parsed.payrollRuns ? 'backup' : parsed.employees ? 'employees' : parsed.settings ? 'settings' : null);
  if (!type) throw new Error('Unrecognized JSON structure');

  const importEmployees = !document.getElementById('io-import-employees') || document.getElementById('io-import-employees').checked;
  const importRuns      = !document.getElementById('io-import-runs')      || document.getElementById('io-import-runs').checked;
  const importSettings  = !document.getElementById('io-import-settings')  || document.getElementById('io-import-settings').checked;
  const importLayout    = !document.getElementById('io-import-layout')    || document.getElementById('io-import-layout').checked;

  let msgs = [];

  if ((type === 'backup' || type === 'employees') && parsed.employees && importEmployees) {
    let added = 0, updated = 0;
    parsed.employees.forEach(emp => {
      const idx = employees.findIndex(e => e.empId === emp.empId);
      if (idx >= 0) { employees[idx] = { ...emp, id: employees[idx].id }; updated++; }
      else { employees.push({ ...emp, id: emp.id || Date.now() + Math.random() }); added++; }
    });
    saveEmployees();
    msgs.push(`${added} employees added, ${updated} updated`);
  }

  if ((type === 'backup' || type === 'payrollRuns') && parsed.payrollRuns && importRuns) {
    let added = 0, updated = 0;
    parsed.payrollRuns.forEach(run => {
      const idx = payrollRuns.findIndex(r => r.month === run.month && r.year === run.year);
      if (idx >= 0) { payrollRuns[idx] = run; updated++; }
      else { payrollRuns.push(run); added++; }
    });
    saveRuns();
    msgs.push(`${added} payroll runs added, ${updated} updated`);
  }

  if ((type === 'backup' || type === 'settings') && parsed.settings && importSettings) {
    settings = { ...settings, ...parsed.settings };
    saveSettingsData();
    msgs.push('Settings imported');
  }

  if ((type === 'backup' || type === 'settings') && parsed.layoutConfig && importLayout) {
    const importedLc = parsed.layoutConfig;
    layoutConfig = { ...layoutConfig, ...importedLc };
    // If the backup contains a logo, compress it and save to IndexedDB + _logoCache
    if (importedLc.logoData && importedLc.logoData.startsWith('data:')) {
      compressAndCacheLogo(importedLc.logoData).then(() => {
        msgs.push('Layout config imported');
      });
    } else {
      saveLayoutData();
      msgs.push('Layout config imported');
    }
  }

  updateDashboard(); renderEmployees(); renderPayslips();
  toast(msgs.join(' · ') || 'Nothing imported.', 'green');
}

