// ── Import: handle file selection ──────────────────────────
function handleImportFile(event) {
  const file = event.target.files[0];
  if (!file) return;

  const preview = document.getElementById('io-import-preview');
  const importBtn = document.getElementById('io-import-btn');
  preview.classList.add('hidden');
  importBtn.disabled = true;
  importBtn.textContent = 'Import';
  window._importData = null;

  const ext = file.name.split('.').pop().toLowerCase();
  const reader = new FileReader();

  reader.onload = (e) => {
    const content = e.target.result;

    if (ext === 'json') {
      try {
        const parsed = JSON.parse(content);
        window._importData = { type: 'json', parsed, filename: file.name };
        renderImportPreview(parsed, file.name);
      } catch {
        showImportError('Invalid JSON file — could not parse.');
      }
    } else if (ext === 'csv') {
      window._importData = { type: 'csv', content, filename: file.name };
      renderCSVPreview(content, file.name);
    } else {
      showImportError('Unsupported file type. Please use .json or .csv');
    }
  };

  reader.readAsText(file);
}

